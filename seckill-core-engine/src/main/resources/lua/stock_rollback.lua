-- ============================================================================
-- stock_rollback.lua - 库存回补原子脚本 (修正版)
-- ============================================================================
-- 功能: 精确回补到用户扣减的原分片，避免分片库存溢出
-- 
-- 设计原则:
--   1. 回补必须精确恢复到原分片，不能随机分配
--   2. 从Hash中读取用户扣减的分片索引
--   3. 回补完成后清除购买记录
--
-- KEYS:
--   KEYS[1]: 用户分片记录Hash (seckill:user_shard:{seckillId})
--   KEYS[2]: 购买记录Set (seckill:bought:{seckillId})
--   KEYS[3]: 分片库存Key前缀 (seckill:stock:{seckillId}:shard:)
-- 
-- ARGV:
--   ARGV[1]: userId
--   ARGV[2]: quantity
--   ARGV[3]: shardCount
-- 
-- 返回值:
--   -1: 未找到分片记录（异常情况，需人工处理）
--    0: 用户未购买（无需回补）
--   >0: 成功，返回回补的分片索引
-- ============================================================================

local userShardKey = KEYS[1]
local boughtKey = KEYS[2]
local shardPrefix = KEYS[3]

local userId = ARGV[1]
local quantity = tonumber(ARGV[2])
local shardCount = tonumber(ARGV[3])

-- ============================================================================
-- Step 1: 检查用户是否已购买
-- ============================================================================
if redis.call('SISMEMBER', boughtKey, userId) == 0 then
    return 0  -- 用户未购买，无需回补
end

-- ============================================================================
-- Step 2: 获取用户扣减的分片索引
-- ============================================================================
-- 关键：从Hash中读取用户扣减的原分片
-- 这是精确回补的核心，避免分片库存溢出
local shardIndex = tonumber(redis.call('HGET', userShardKey, userId))

-- 校验分片索引有效性
if shardIndex == nil or shardIndex < 0 or shardIndex >= shardCount then
    return -1  -- 异常: 未找到分片记录或索引无效
end

-- ============================================================================
-- Step 3: 回补到原分片（精确恢复）
-- ============================================================================
-- 注意：这里回补到用户原扣减的分片
-- 不是随机分配，避免某分片库存溢出
local shardKey = shardPrefix .. shardIndex
redis.call('INCRBY', shardKey, quantity)

-- ============================================================================
-- Step 4: 清除购买记录
-- ============================================================================
-- 回补完成后，清除购买记录和分片记录
-- 允许用户再次购买（如果活动还在进行）
redis.call('SREM', boughtKey, userId)
redis.call('HDEL', userShardKey, userId)

-- ============================================================================
-- Step 5: 返回成功
-- ============================================================================
-- 返回值 = 1000 + 分片索引（避免 shard 0 与"用户未购买"的 0 冲突）
-- Java 端解析：result >= 1000 表示成功，shardIndex = result - 1000
return 1000 + shardIndex